package org.gdarrigo.straight_line.cartesian.plane;

public final class Point {

    private Integer x;

    private Integer y;

    public Point(Integer x, Integer y) {
        this.x = x;
        this.y = y;
    }

    public Integer getX() {
        return x;
    }

    public Integer getY() {
        return y;
    }
}
